# ROI preset: ambil bagian tengah 60% lebar & tinggi
ROI_CENTER_FRACTION = 0.60   # ubah 0.5–0.8 sesuai kebutuhan
CONF   = 0.001
IMGSZ  = 896

def crop_center_roi(src_path, frac=0.6):
    im = Image.open(src_path).convert("RGB")
    W, H = im.size
    w = int(W * frac)
    h = int(H * frac)
    x1 = (W - w) // 2
    y1 = (H - h) // 2
    x2 = x1 + w
    y2 = y1 + h
    roi = im.crop((x1, y1, x2, y2))
    # simpan sementara
    tmp_path = f"/content/tmp_roi_{uuid.uuid4().hex}.jpg"
    roi.save(tmp_path, quality=95)
    return tmp_path, (x1, y1, x2, y2), (W, H)

