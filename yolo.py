def tight_bbox_from_alpha(pil_img):
    """Kembalikan (xmin,ymin,xmax,ymax) berdasarkan alpha channel.
       Jika tak ada alpha/non-zero, fallback seluruh gambar."""
    w, h = pil_img.size
    if pil_img.mode in ("RGBA","LA"):
        alpha = pil_img.split()[-1]
        bbox = alpha.getbbox()  # (l,u,r,b) atau None jika kosong
        if bbox is not None:
            return bbox
    return (0, 0, w, h)

def yolo_normalize(xmin, ymin, xmax, ymax, w, h):
    cx = (xmin + xmax) / 2 / w
    cy = (ymin + ymax) / 2 / h
    bw = (xmax - xmin) / w
    bh = (ymax - ymin) / h
    return cx, cy, bw, bh

# Kumpulkan file per kelas, lalu split PER KELAS (agar distribusi seimbang)
supported = [".png", ".jpg", ".jpeg", ".webp"]
total_train = 0
total_val = 0
skipped = 0

for cls in classes:
    cls_dir = SRC / cls
    files = []
    for ext in supported:
        files += glob(str(cls_dir / f"*{ext}"))
    files = sorted(files)
    random.shuffle(files)

    n_val = max(1, int(len(files) * VAL_RATIO))  # jaga-jaga minimal 1
    val_files = set(files[:n_val])
    train_files = files[n_val:]

    for f in tqdm(files, desc=f"[{cls}]"):
        split = "val" if f in val_files else "train"
        try:
            img = Image.open(f)
            # Pastikan ada channel untuk alpha check
            if img.mode not in ("RGBA","LA"):
                # Untuk JPG tanpa alpha, konversi ke RGBA agar mudah, alpha akan full-opaque
                img = img.convert("RGBA")
            w, h = img.size
            xmin, ymin, xmax, ymax = tight_bbox_from_alpha(img)
            xmin, ymin = max(0, xmin), max(0, ymin)
            xmax, ymax = min(w, xmax), min(h, ymax)
            if xmax <= xmin or ymax <= ymin:
                skipped += 1
                continue

            cx, cy, bw, bh = yolo_normalize(xmin, ymin, xmax, ymax, w, h)
            cls_id = class_to_id[cls]

            # Salin gambar
            dst_img = OUT / "images" / split / Path(f).name
            shutil.copy2(f, dst_img)

            # Tulis label
            dst_lbl = OUT / "labels" / split / (Path(f).stem + ".txt")
            with open(dst_lbl, "w") as lf:
                lf.write(f"{cls_id} {cx:.6f} {cy:.6f} {bw:.6f} {bh:.6f}\n")

            if split == "train": total_train += 1
            else: total_val += 1

        except Exception as e:
            skipped += 1
            print("Skip (error):", f, "->", e)

# Tulis data.yaml
