def shrink_box(xyxy, shrink_ratio, W, H):
    """xyxy: [x1,y1,x2,y2], shrink_ratio: 0.0-0.4 umum, W,H: ukuran gambar"""
    x1, y1, x2, y2 = xyxy
    w = x2 - x1
    h = y2 - y1
    dx = w * shrink_ratio
    dy = h * shrink_ratio
    nx1 = max(0, x1 + dx)
    ny1 = max(0, y1 + dy)
    nx2 = min(W, x2 - dx)
    ny2 = min(H, y2 - dy)
    if nx2 <= nx1 or ny2 <= ny1:  # fallback kalau terlalu kecil
        return [x1, y1, x2, y2]
    return [nx1, ny1, nx2, ny2]

SHRINK = 0.10   # 10% tiap sisi; ubah ke 0.05/0.2 sesuai selera
CONF   = 0.01   # longgarkan kalau sering “no detections”
IMGSZ  = 896    # naikkan untuk objek kecil

