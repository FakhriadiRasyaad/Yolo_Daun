        cls_id = int(b.cls.item())
        conf = float(b.conf.item())
        x1, y1, x2, y2 = [float(x) for x in b.xyxy[0].tolist()]
        tight = shrink_box([x1, y1, x2, y2], SHRINK, W, H)

        cx1, cy1, cx2, cy2 = [int(round(v)) for v in tight]
        crop = im.crop((cx1, cy1, cx2, cy2))
        crop_path = os.path.join(save_dir, f"{os.path.splitext(os.path.basename(fname))[0]}_crop{i}_{model.names[cls_id]}_{conf:.2f}.jpg")
